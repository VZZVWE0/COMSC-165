#include <iostream>

using namespace std;

int main () {


    cout << "The current ocean level is 3.4mm and is rising at 1.5mm per year." << endl;

    double rise5=1.5*5-3.4;

    double rise7=1.5*7-3.4;

    cout << "The ocean level will be " << rise5 << "mm higher in 5 years." << endl;

    cout << "The ocean level will be " << rise7 << "mm higher in 7 years." << endl;

return 0;

}
